// Escreva um algoritmo para ler um valor (do teclado) e escrever (na tela) o seu antecessor.

// Campo do exercício 05
let valor;  //variável

alert("Qual o antecessor do numero..."); //saída de dados
numero1=parseInt(prompt("Digite um número: ")); //entrada e saída de dados

valor=numero1-1; //Processamento

alert("O antecessor do numero "+numero1+" é: "+valor); //saída de dados