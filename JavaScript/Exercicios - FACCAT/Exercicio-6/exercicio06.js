// Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a área do retângulo.

// Campo do exercício 06
let base, altura, area;  //variável

alert("Calculando a área do retângulo"); //saída de dados
base=parseFloat(prompt("Digite um número da base: ")); //entrada e saída de dados
altura=parseFloat(prompt("Digite um número da base: ")); //entrada e saída de dados

area=base*altura; //Processamento

alert("A área do retângulo é: "+area+"cm²"); //saída de dados