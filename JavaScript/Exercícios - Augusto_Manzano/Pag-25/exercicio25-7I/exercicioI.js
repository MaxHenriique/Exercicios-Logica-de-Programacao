// Exercícios, Lógica de Programação, Algoritmos e JavaScript
// Ler dois inteiros (variáveis A e B) e imprimir o resultado do quadrado da diferença do primeiro valor pelo segundo.


// Campo de variável
let A,B,C,D;

// Campo de algoritmo
alert("Calculando e mostrando o resultado ao quadrado"); //Saída de dados

A=parseFloat(prompt("Digite o primeiro valor: ")); //Entrada e Saída de dados
B=parseFloat(prompt("Digite o segundo valor: ")); //Entrada e Saída de dados

C=A-B; //Processamento
D=C*C; //Processamento

alert("O resultado é: "+D+"²"); //Saída de dados