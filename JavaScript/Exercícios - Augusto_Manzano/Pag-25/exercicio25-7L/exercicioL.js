// Elaborar um programa que efetue a leitura de três valores (A, B e C) e apresente como resultado final à soma dos quadrados dos três valores lidos.


// Campo de variável
let A,B,C,D;

// Campo de algoritmo
alert("Somando os quadrados dos três valores"); //Saída de dados

A=parseFloat(prompt("Digite o valor de A: ")); //Entrada e Saída de dados
B=parseFloat(prompt("Digite o valor de B: ")); //Entrada e Saída de dados
C=parseFloat(prompt("Digite o valor de C: ")); //Entrada e Saída de dados

A=A*A; //Processamento
B=B*B; //Processamento
C=C*C; //Processamento

D=A+B+C //Processamento

alert("O resultado é: "+D+"²"); //Saída de dados